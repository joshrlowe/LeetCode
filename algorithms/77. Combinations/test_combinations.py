import pytest  # type: ignore
from combinations import combine
from typing import List


@pytest.mark.parametrize(
    "n, k, expected",
    [
        (1, 1, [[1]]),
        (2, 1, [[1], [2]]),
        (2, 2, [[1, 2]]),
        (3, 2, [[1, 2], [1, 3], [2, 3]]),
        (3, 1, [[1], [2], [3]]),
        (4, 2, [[1, 2], [1, 3], [1, 4], [2, 3], [2, 4], [3, 4]]),
        (4, 3, [[1, 2, 3], [1, 2, 4], [1, 3, 4], [2, 3, 4]]),
        (4, 4, [[1, 2, 3, 4]]),
        (
            5,
            3,
            [
                [1, 2, 3],
                [1, 2, 4],
                [1, 2, 5],
                [1, 3, 4],
                [1, 3, 5],
                [1, 4, 5],
                [2, 3, 4],
                [2, 3, 5],
                [2, 4, 5],
                [3, 4, 5],
            ],
        ),
        (
            5,
            2,
            [
                [1, 2],
                [1, 3],
                [1, 4],
                [1, 5],
                [2, 3],
                [2, 4],
                [2, 5],
                [3, 4],
                [3, 5],
                [4, 5],
            ],
        ),
        (5, 1, [[1], [2], [3], [4], [5]]),
        (5, 0, [[]]),
        (
            6,
            3,
            [
                [1, 2, 3],
                [1, 2, 4],
                [1, 2, 5],
                [1, 2, 6],
                [1, 3, 4],
                [1, 3, 5],
                [1, 3, 6],
                [1, 4, 5],
                [1, 4, 6],
                [1, 5, 6],
                [2, 3, 4],
                [2, 3, 5],
                [2, 3, 6],
                [2, 4, 5],
                [2, 4, 6],
                [2, 5, 6],
                [3, 4, 5],
                [3, 4, 6],
                [3, 5, 6],
                [4, 5, 6],
            ],
        ),
        (
            6,
            2,
            [
                [1, 2],
                [1, 3],
                [1, 4],
                [1, 5],
                [1, 6],
                [2, 3],
                [2, 4],
                [2, 5],
                [2, 6],
                [3, 4],
                [3, 5],
                [3, 6],
                [4, 5],
                [4, 6],
                [5, 6],
            ],
        ),
        (6, 1, [[1], [2], [3], [4], [5], [6]]),
        (6, 0, [[]]),
    ],
)
def test_combine(n: int, k: int, expected: List[List[int]]) -> None:
    result = combine(n, k)
    assert result == expected, f"n: {n}, k: {k}, expected: {expected}, result: {result}"
